using System;

namespace BasicMethods
{
    public class Operations
    {
        public static int AddTen(int num)
        {
            return num + 10;
        }
        public static int MultiplyTen(int num)
        {
            return num * 10;
        }
        public static double DivideTen(int num)
        {
            return (double)num / 10;
        }
    }
}